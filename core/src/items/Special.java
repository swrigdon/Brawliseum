package items;

/**
 *
 * @author Jason
 */
public class Special 
{
    private String name;
    private float attackBuff;
    private float defenseBuff;
    private float healthBuff;
    private float energyBuff;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the attackBuff
     */
    public float getAttackBuff() {
        return attackBuff;
    }

    /**
     * @param attackBuff the attackBuff to set
     */
    public void setAttackBuff(float attackBuff) {
        this.attackBuff = attackBuff;
    }

    /**
     * @return the defenseBuff
     */
    public float getDefenseBuff() {
        return defenseBuff;
    }

    /**
     * @param defenseBuff the defenseBuff to set
     */
    public void setDefenseBuff(float defenseBuff) {
        this.defenseBuff = defenseBuff;
    }

    /**
     * @return the healthBuff
     */
    public float getHealthBuff() {
        return healthBuff;
    }

    /**
     * @param healthBuff the healthBuff to set
     */
    public void setHealthBuff(float healthBuff) {
        this.healthBuff = healthBuff;
    }

    /**
     * @return the energyBuff
     */
    public float getEnergyBuff() {
        return energyBuff;
    }

    /**
     * @param energyBuff the energyBuff to set
     */
    public void setEnergyBuff(float energyBuff) {
        this.energyBuff = energyBuff;
    }
    
    
}
